                if (theme.settings.flashType != 'none') {
                    function updateCountdown() {
                        const now = new Date();
                        if (theme.settings.flashType == 'date') {
                            const deadline = new Date(theme.settings.deadlineDate);
                            if (now > deadline) {
                                document.getElementById('countdown').textContent = 'Hết hạn';
                                return;
                            }
                            const timeDiff = deadline - now;
                            const hours = Math.floor((timeDiff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
                            const minutes = Math.floor((timeDiff % (1000 * 60 * 60)) / (1000 * 60));
                            const seconds = Math.floor((timeDiff % (1000 * 60)) / 1000);
                            const formattedTime =
                                `<h4>Kết thúc sau: </h4> ${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:${String(seconds).padStart(2, '0')}`;
                            document.getElementById('countdown').innerHTML = formattedTime;
                        } else if (theme.settings.flashType == 'day') {
                            const targetTime = new Date(now);
                            targetTime.setHours(24, 0, 0, 0);
                            const timeDiff = targetTime - now;
                            const hours = Math.floor((timeDiff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
                            const minutes = Math.floor((timeDiff % (1000 * 60 * 60)) / (1000 * 60));
                            const seconds = Math.floor((timeDiff % (1000 * 60)) / 1000);
                            const formattedTime =
                                `<h4>Kết thúc sau: </h4> ${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:${String(seconds).padStart(2, '0')}`;
                            document.getElementById('countdown').innerHTML = formattedTime;
                        }
                    }
                    setInterval(updateCountdown, 1000);
                    updateCountdown();
                }
